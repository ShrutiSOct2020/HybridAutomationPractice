package pojo;

public class ProductDetailsPojo {

	private String productName;
	private String unitRatePrice;
	private String quantity;
	private String size;
	private String color;
	private String productDescription;
	private String totalproductPrice;
	private String totalShipping;
	private String totalPrice;

	public String getProductName() {
		return productName;
	}

	public void setProductName(String productName) {
		this.productName = productName;
	}

	public String getUnitRatePrice() {
		return unitRatePrice;
	}

	public void setUnitRatePrice(String unitRatePrice) {
		this.unitRatePrice = unitRatePrice;
	}

	public String getQuantity() {
		return quantity;
	}

	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}

	public String getSize() {
		return size;
	}

	public void setSize(String size) {
		this.size = size;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public String getProductDescription() {
		return productDescription;
	}

	public void setProductDescription(String productDescription) {
		this.productDescription = productDescription;
	}

	public String getTotalproductPrice() {
		return totalproductPrice;
	}

	public void setTotalproductPrice(String totalproductPrice) {
		this.totalproductPrice = totalproductPrice;
	}

	public String getTotalShipping() {
		return totalShipping;
	}

	public void setTotalShipping(String totalShipping) {
		this.totalShipping = totalShipping;
	}

	public String getTotalPrice() {
		return totalPrice;
	}

	public void setTotalPrice(String totalPrice) {
		this.totalPrice = totalPrice;
	}

}
